# 使用sort方法对列表进行永久排序
cars = ['bmw','audi','toyouta','subaru']
cars.sort(reverse=True)
print(cars)

# 使用sorted函数对数组进行排序
cars = ['bmw','audi','toyouta','subaru']
print("Here is the original list:")
print(cars)
print("Here is the sorted list")
print(sorted(cars))
print("Here is the orginal list again")
print(cars)
# 使数组倒序
cars.reverse()
print(cars)

# 确定列表长度
cars = ['bmw','audi','toyouta','subaru']
print(len(cars))

